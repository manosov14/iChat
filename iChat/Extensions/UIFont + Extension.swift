import UIKit

extension UIFont {
    
    static func avenir20() -> UIFont? {
        let font = UIFont.init(name: "avenir", size: 20)
        return font
    }
    
    static func avenir26() -> UIFont? {
        let font = UIFont.init(name: "avenir", size: 26)
        return font
    }
    
}
